// Faça cinco programas, um para cada operação aritmética básica. Seu programa deve ter duas constantes, a e b, definidas no começo com os valores que serão operados. Faça programas para:

// Adição (a + b)
// Subtração (a - b)
// Multiplicação (a * b)
// Divisão (a / b)
// Módulo (a % b)

function adicao(n1, n2) {
  return "Adição = " + (n1 + n2);
}
console.log(adicao(12, 12));


function subtracao(n1, n2) {
  return "Subtração = " + (n1 - n2);
}
console.log(subtracao(20, 12));



function multiplicação(n1, n2) {
  return "Multiplicação = " + n1 * n2;
}
console.log(multiplicação(5, 5));



function divisao(n1,n2){
  return "Divisão = " + n1 / n2;
}
console.log(divisao(12,2));



function modulo(n1,n2){
  return "Modúlo = " + n1 % n2;
}
console.log(modulo(55,2));